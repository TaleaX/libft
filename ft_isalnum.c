int	ft_isalnum(int c)
{
	unsigned char	cha;

	cha = (unsigned char) c;
	return ((cha >= 'A' && cha <= 'Z') || (cha >= 'a' && cha <= 'z')
		|| (cha >= '0' && cha <= '9'));
}
