#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*tmp;

	tmp = *lst;
	while (tmp)
	{
		tmp = tmp->next;
		ft_lstdelone(*lst, del);
		(*lst) = tmp;
	}
	free(lst);
}
